package com.cobrain.android.model;

public class Badge {
	int _id;
	String code;
	String title;
	int created_at;
	int priority;
	
	public int getId() {
		return _id;
	}
	public String getCode() {
		return code;
	}
	public String getTitle() {
		return title;
	}
	public int getCreatedAt() {
		return created_at;
	}
	public int getPriority() {
		return priority;
	}
	
}
